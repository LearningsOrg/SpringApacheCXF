package com.ge.ae.diagnostics.service.cnr.data.api.models;

public class EngineVO {
	String position;
	String esn;
	String engineType;
	String engineInstallDate;
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getEsn() {
		return esn;
	}
	public void setEsn(String esn) {
		this.esn = esn;
	}
	public String getEngineType() {
		return engineType;
	}
	public void setEngineType(String engineType) {
		this.engineType = engineType;
	}
	public String getEngineInstallDate() {
		return engineInstallDate;
	}
	public void setEngineInstallDate(String engineInstallDate) {
		this.engineInstallDate = engineInstallDate;
	}


	
}
