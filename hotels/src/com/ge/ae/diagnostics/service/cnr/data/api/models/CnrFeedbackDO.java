package com.ge.ae.diagnostics.service.cnr.data.api.models;

public class CnrFeedbackDO {
	String mxStaus;
	String mxDate;
	String remarks;
	String isEffective;
	String esn;
	String cnrNumber;
	String stepNumber;
	public String getMxStaus() {
		return mxStaus;
	}
	public void setMxStaus(String mxStaus) {
		this.mxStaus = mxStaus;
	}
	public String getMxDate() {
		return mxDate;
	}
	public void setMxDate(String mxDate) {
		this.mxDate = mxDate;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getIsEffective() {
		return isEffective;
	}
	public void setIsEffective(String isEffective) {
		this.isEffective = isEffective;
	}
	public String getEsn() {
		return esn;
	}
	public void setEsn(String esn) {
		this.esn = esn;
	}
	public String getCnrNumber() {
		return cnrNumber;
	}
	public void setCnrNumber(String cnrNumber) {
		this.cnrNumber = cnrNumber;
	}
	public String getStepNumber() {
		return stepNumber;
	}
	public void setStepNumber(String stepNumber) {
		this.stepNumber = stepNumber;
	}
	
}
